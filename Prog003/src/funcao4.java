public class funcao4 {

    //faça uma função int dobro(n) que retorne o dobro de n

    private static int dobro(int n) {
        return n*2;
    }

    public static void main(String[] args) {
        System.out.println(dobro(3));
        System.out.println(dobro(8));
        int x = dobro(1);
        System.out.println(x);

    }
}


